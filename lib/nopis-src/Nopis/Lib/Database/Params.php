namespace Nopis\Lib\Database;

/**
 * @author Wangbin
 */
class Params
{

    /**
     * @var array
     */
    private $params = [];

    /**
     * @var string
     */
    private $query = '';

    /**
     * @param array $params
     */
    public function setParams(array $params)
    {
        $this->params = $params;
    }

    /**
     * @return array
     */
    public function getParams()
    {
        return $this->params;
    }

    /**
     * @param string $query
     */
    public function setQuery($query)
    {
        $this->query = $query;
    }

    /**
     * @return string
     */
    public function getQuery()
    {
        return $this->query;
    }

    /**
     * @return string
     */
    public function __toString()
    {
        return $this->getQuery();
    }

}
